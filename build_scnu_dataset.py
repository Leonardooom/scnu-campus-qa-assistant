def parse_rows():
    rows = []
    sources = []
    for category, block in RAW.items():
        source_url, volatile = SOURCES[category]
        for index, line in enumerate(block.strip().splitlines()):
            question, answer = [part.strip() for part in line.split("|", 1)]
            if len(answer) < 60:
                answer += CATEGORY_NOTES[category][index % len(CATEGORY_NOTES[category])]
            rows.append({"instruction": INSTRUCTION, "input": question, "output": answer})
            sources.append({
                "category": category,
                "question": question,
                "official_source": source_url,
                "verified_on": VERIFIED,
                "volatile": volatile,
            })
    return rows, sources


